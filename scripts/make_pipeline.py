#!/usr/bin/env python3
"""Generate an AgentFlow pipeline for Claude-led Codex implementation review."""

from __future__ import annotations

import argparse
from datetime import datetime
from pathlib import Path


RUNS_ROOT = Path.home() / ".codex" / "teamflow" / "runs"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate a Claude/Codex AgentFlow pipeline.")
    parser.add_argument("--repo", required=True, help="Target repository path.")
    parser.add_argument("--plan", required=True, help="Plan markdown file.")
    parser.add_argument("--run-id", default=None, help="Stable run id. Defaults to timestamp.")
    parser.add_argument("--claude-model", default="opus", help="Claude model label for prompt metadata.")
    parser.add_argument("--max-review-rounds", type=int, default=5, help="Maximum review loop iterations.")
    parser.add_argument("--use-worktree", action="store_true", help="Ask AgentFlow to isolate nodes in worktrees.")
    parser.add_argument("--output", default=None, help="Output pipeline path.")
    return parser.parse_args()


def escape_triple(text: str) -> str:
    return text.replace('"""', '\\"\\"\\"')


def pipeline_text(repo: Path, plan: Path, args: argparse.Namespace) -> str:
    repo_literal = str(repo)
    plan_literal = str(plan)
    use_worktree = "True" if args.use_worktree else "False"
    graph_name = f"claude-codex-teamflow-{args.run_id}"

    return f'''# Generated by claude-codex-teamflow.
# Validate with: agentflow validate "{args.output}"

from agentflow import Graph, claude, codex, shell

REPO = r"""{escape_triple(repo_literal)}"""
PLAN_FILE = r"""{escape_triple(plan_literal)}"""

with Graph(
    "{graph_name}",
    concurrency=1,
    max_iterations={args.max_review_rounds},
    fail_fast=True,
    use_worktree={use_worktree},
) as g:
    claude_plan = claude(
        task_id="claude_plan",
        tools="read_only",
        timeout_seconds=900,
        prompt=f"""
You are Claude Code Opus acting as team lead.

Repo: {{REPO}}
Plan file: {{PLAN_FILE}}
Model label: {args.claude_model}

Read the plan and return a decision-complete implementation brief for Codex.
Include success criteria, QA commands, review criteria, and any blockers.
""",
    )

    codex_impl = codex(
        task_id="codex_implementation",
        tools="read_write",
        timeout_seconds=3600,
        prompt="""
You are Codex acting as full-stack implementation and QA team.

Implement Claude's brief:
{{{{ nodes.claude_plan.output }}}}

{{% if nodes.claude_review.output %}}
Claude requested changes in the prior review:
{{{{ nodes.claude_review.output }}}}
{{% endif %}}

Rules:
- Work in the repo only.
- Keep changes scoped to the plan.
- Run the requested tests.
- Do not commit.
- Finish with a review packet listing changed files, QA evidence, risks, and blockers.
""",
    )

    qa_summary = shell(
        task_id="qa_summary",
        script=f"""
git -C "{{REPO}}" status --short
git -C "{{REPO}}" diff --stat
""",
    )

    claude_review = claude(
        task_id="claude_review",
        tools="read_only",
        timeout_seconds=1800,
        success_criteria=[{{"kind": "output_contains", "value": "APPROVED"}}],
        prompt="""
You are Claude Code Opus acting as final reviewer.

Review Codex output:
{{{{ nodes.codex_implementation.output }}}}

QA summary:
{{{{ nodes.qa_summary.output }}}}

Return one exact status line:
- STATUS: APPROVED
- STATUS: CHANGES_REQUESTED
- STATUS: BLOCKED

Only approve if the implementation satisfies the plan and QA is credible.
For requested changes, list actionable fixes.
""",
    )

    claude_plan >> codex_impl >> qa_summary >> claude_review
    claude_review.on_failure >> codex_impl

print(g.to_json())
'''


def main() -> int:
    args = parse_args()
    repo = Path(args.repo).resolve()
    plan = Path(args.plan).resolve()
    if not repo.is_dir():
        raise SystemExit(f"Repo directory not found: {repo}")
    if not plan.is_file():
        raise SystemExit(f"Plan file not found: {plan}")
    if args.run_id is None:
        args.run_id = datetime.now().strftime("%Y%m%d-%H%M%S")
    if args.output is None:
        args.output = str(RUNS_ROOT / f"{repo.name}-{args.run_id}" / "pipeline.py")

    output = Path(args.output)
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(pipeline_text(repo, plan, args), encoding="utf-8")
    print(output)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
